import sys


class Node:
    def __init__(self, val, min_val=None, prev_node=None):
        self.val = val
        if min_val:
            self.min_val = min(val, min_val)
        else:
            self.min_val = val
        self.prev_node = prev_node


class LinkedList:
    def __init__(self):
        self.head = None
        self.tail = None
        self.size = 0

    def insert_val(self, val):
        if self.size == 0:
            new_node = Node(val)
            self.head = new_node
            self.tail = new_node
        else:
            new_node = Node(val, self.tail.min_val, self.tail)
            self.tail = new_node
        self.size += 1

    def erase_tail(self):
        if self.size == 1:
            self.head = None
            self.tail = None
        else:
            self.tail = self.tail.prev_node
        self.size -= 1


class Stack(LinkedList):
    def get_min_val(self):
        return self.tail.min_val


def main():
    n = int(sys.stdin.readline())
    stack = Stack()
    for _ in range(n):
        operation = sys.stdin.readline().split()
        if operation[0] == '1':
            stack.insert_val(int(operation[1]))
        elif operation[0] == '2':
            stack.erase_tail()
        else:
            print(stack.get_min_val())


if __name__ == "__main__":
    main()
